// Print the numbers from 1 to 10 to the console using loop

console.log("Using For Loop");

for(let i=1; i<=10; i++) {
    console.log(i);
}

console.log("Using while loop");

let i = 1;
while(i<=10) {
    console.log(i);
    i++;
}

console.log("Using do while loop");

let j=1;

do {
    console.log(j);
    j++;
}
while(j<=10);